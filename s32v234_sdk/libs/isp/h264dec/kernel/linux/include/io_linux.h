#ifndef H264DCDIOLINUX_H
#define H264DCDIOLINUX_H

#include <asm/io.h>
#include <linux/types.h>

static inline uint32_t reg_read32(const uint32_t *reg_addr)
{
    return (uint32_t)readl(reg_addr);
}

static inline void reg_write32(uint32_t *reg_addr, const uint32_t value)
{
    writel(value, reg_addr);
}

#endif /* H264DCDIOLINUX_H */
